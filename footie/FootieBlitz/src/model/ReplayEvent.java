package model;

public class ReplayEvent {
	String time = "";
	String score = "";
	String description = "";
	int frame;
	
	public ReplayEvent(String time, String score, String description, int frame) {
		super();
		this.time = time;
		this.score = score;
		this.description = description;
		this.frame = frame;
	}

	public String getTime() {
		return time;
	}

	public void setTime(String time) {
		this.time = time;
	}

	public String getScore() {
		return score;
	}

	public void setScore(String score) {
		this.score = score;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public int getFrame() {
		return frame;
	}

	public void setFrame(int frame) {
		this.frame = frame;
	}
	
	
}
